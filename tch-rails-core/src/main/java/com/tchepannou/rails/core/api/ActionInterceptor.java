package com.tchepannou.rails.core.api;

import java.io.IOException;
import java.lang.reflect.Method;

/**
 *
 * @author herve
 */
public interface ActionInterceptor
    extends Interceptor
{
    //-- Public methods
    /**
     * This method is called prior accessing a controller
     *
     * @param controller controller
     */
    public int before (ActionController controller)
        throws IOException;

    /**
     * This method is called after accessing a controller
     *
     * @param controller controller
     */
    public void after (ActionController controller)
        throws IOException;

    /**
     * This method is called prior accessing an action method of a controller
     *
     * @param controller controller
     * @param method action method
     */
    public int before (Method method, ActionController controller)
        throws IOException;

    /**
     * This method is called after accessing an action method of a controller
     *
     * @param controller controller
     * @param method action method
     */
    public void after (Method method, ActionController controller, Throwable e)
        throws IOException;
}
